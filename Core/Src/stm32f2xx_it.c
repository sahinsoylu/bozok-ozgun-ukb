void HAL_UART_RxIdleCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == UART4) {

        HAL_UART_DMAStop(huart);

        received_data_len = UART_RX_DMA_BUFFER_SIZE - huart->hdmarx->Instance->NDTR;

        if (received_data_len == 5) {
            HAL_GPIO_TogglePin(LED_5BYTE_GPIO_Port, LED_5BYTE_Pin);

            if(huart4_rx_dma_buffer[1] == 0x20){
            	g_systemControl.ukb_mode = SIT;
            	g_flightData.isDelayed = 1;
            }else if(huart4_rx_dma_buffer[1] == 0x22){
            	reset_state();
            	g_systemControl.ukb_mode = SUT;
            }else if(huart4_rx_dma_buffer[1] == 0x24){
            	reset_state();
            	g_systemControl.ukb_mode = NORMAL;

            }

        } else if (received_data_len == 36) {

        }
        HAL_UART_Receive_DMA(huart, huart4_rx_dma_buffer, UART_RX_DMA_BUFFER_SIZE);
    }
}

void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == UART4) {

        HAL_UART_DMAStop(huart);
        HAL_UART_Receive_DMA(huart, huart4_rx_dma_buffer, UART_RX_DMA_BUFFER_SIZE);
    }
}

